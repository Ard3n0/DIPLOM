# Каркасы предложений с указанием используемых категорий
TEMPLATES = [
    ("{assertion} устанавливает свойства, которыми обладает {object}.", ["assertion", "object"]),
    ("В рамках современной математики {operation} применяется к {object}.", ["operation", "object"]),
    ("{person} доказал, что для {object} определено {operation}.", ["person", "object", "operation"]),
    ("Согласно {assertion}, {operation} является корректным для {object}.", ["assertion", "operation", "object"]),
    ("Рассмотрим {object} и соответствующее ему {operation}.", ["object", "operation"]),
    ("Классическая {assertion} описывает структуру, которую имеет {object}.", ["assertion", "object"]),
    ("{person} предложил использовать {operation} для анализа {object}.", ["person", "operation", "object"]),
    ("Исследование {object} невозможно без такой операции, как {operation}.", ["object", "operation"]),
    ("В работе {person} подробно изучается {object}.", ["person", "object"]),
    ("Используя {operation}, мы доказываем, что {assertion} выполняется.", ["operation", "assertion"]),
]

def main():
    generated_data = []
    print("Генерация обучающей выборки (300 примеров)...")

    for _ in range(300):
        # Выбор случайного шаблона и требуемых ключей
        tpl_text, tpl_keys = random.choice(TEMPLATES)
        
        # Подбор терминов для текущего предложения
        mapping = {
            "person": random.choice(PERSONS),
            "object": random.choice(OBJECTS),
            "operation": random.choice(OPERATIONS),
            "assertion": random.choice(ASSERTIONS)
        }
        
        final_text = tpl_text.format(**mapping)
        entities = []
        
        # Вычисление символьных индексов для разметки
        for key in tpl_keys:
            value = mapping[key]
            start = final_text.find(value)
            
            if start != -1:
                end = start + len(value)
                entities.append({
                    "start": start,
                    "end": end,
                    "label": key.upper()
                })
        
        generated_data.append({
            "text": final_text,
            "entities": entities
        })

    # Сохранение датасета
    output_dir = "data"
    os.makedirs(output_dir, exist_ok=True)

    output_file = os.path.join(output_dir, "generated_300.json")
    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(generated_data, f, ensure_ascii=False, indent=4)

    print(f"Файл сохранен: {output_file}")

if __name__ == "__main__":
    main()